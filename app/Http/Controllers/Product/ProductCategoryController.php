     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Product  $product
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Product $product, Category $category)
    {
        // attach : add category to list and can duplicate 
        // sync : delete all and add the given one
        // syncWithoutDetaching : add category to list and don't duplicate (v)
        $product->categories()->syncWithoutDetaching([$category->id]);
        return $this->showAll($product->categories);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Product  $product
     * @return \Illuminate\Http\Response
     */
    public function destroy(Product $product, Category $category)
    {
        // check if the cateegory is attach to product 

        if (!$product->categories()->find($category->id)) :
            //abort(404, 'The specified category is not a category of this product');
            return $this->errorResponse('The specified category is not a category of this product',404);
        endif;
        $product->categories()->detach($category->id);
        return $this->showAll($product->categories);
    }
